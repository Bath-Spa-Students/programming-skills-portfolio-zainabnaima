'''Q5: Write a python program that stores fruit names in a list
named fruits. Unpack the list into three variables x, y and z and
then print the values of each variable.'''

# Store fruit names in a list
fruits = ["Passion fruit", "Mango steen", "Dragon fruit"]

# Unpack the list into three variables x, y, and z
