# Exercise 4:  Large Shirts

def make_shirt(size="Large", message="I love Python"):
    print(f"Creating a {size} T-shirt with the message: '{message}'")

# Large shirt with the default message
make_shirt()

# Medium shirt with the default message
make_shirt("Medium")

# Custom-sized shirt with a different message
make_shirt("Small", "BathSpa is a Blast")
