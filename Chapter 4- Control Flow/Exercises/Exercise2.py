# Exercise 2: Alien Colors
# if block version
alien_color = 'green'

# Check if the alien's color is green.
if alien_color == 'green':
    print("The player just earned 5 points for shooting the alien.")
else:
    # If the alien's color is not green, the player earns 10 points.
    print("The player just earned 10 points.")

# else block version
alien_color = 'red'

# Check if the alien's color is green.
if alien_color == 'green':
    print("The player just earned 5 points for shooting the alien.")
else:
    print("The player just earned 10 points.")
